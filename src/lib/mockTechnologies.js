export const getTechnologiesByCategory = (category) => {
  if (category === 'all') return mockTechnologies;
  return mockTechnologies.filter(tech => tech.category === category);
};

export const getTechnologiesByStatus = (status) => {
  if (status === 'all') return mockTechnologies;
  return mockTechnologies.filter(tech => tech.status === status);
};

export const getTechnologyById = (id) => mockTechnologies.find(tech => tech.id === id);

export const getTechnologyBySlug = (slug) => mockTechnologies.find(tech => tech.slug === slug);

export const getFeaturedTechnologies = () => mockTechnologies.filter(tech => tech.isFeatured);